package modelo;

public class Usuario {
    
    int id;
    boolean adm;
    String nome;
    String cpf;
    String senha;
    String celular;
    String email;
    
    public Usuario(){}
    
    public Usuario(int id, boolean adm, String nome, String cpf, String senha, String celular, String email) {
        this.id = id;
        this.adm = adm;
        this.nome = nome;
        this.cpf = cpf;
        this.senha = senha;
        this.celular = celular;
        this.email = email;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public boolean isAdm() {
        return adm;
    }

    public void setAdm(boolean adm) {
        this.adm = adm;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }

    public String getCelular() {
        return celular;
    }

    public void setCelular(String celular) {
        this.celular = celular;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
    
    
    
}
